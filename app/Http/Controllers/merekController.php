    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $hp = hp::where('id_merek', $id)->get();
        foreach ($hp as $h) {
            hp::destroy($h->id);
        }
        merek::destroy($id);
        return redirect('/');
